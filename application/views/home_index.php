          <h2 class="page-header">快速使用</h2>

          <h5 style="margin-top: 20px;">场景1：机房考试</h5>
          <p>如果是在大学机房里进行在线考试，为避免考生百度或查看本地资料，需要进行以下操作  &nbsp;&nbsp; [ <strong><a href="/home/support">查看图示</a></strong> ]</p>
          <ul>
            <li>1. 请主办方下载本软件，解压后双击编辑Exam.ini，手工配置好考试名称、考试网址、是否锁屏等参数</li>
            <li>2. 将文件夹批量下发到机房的全部电脑上，在考生入场前，启动每台电脑上的Exam.exe即可</li>
          </ul> 
          <h5>场景2：在家考试</h5>
          <p>如果是在家考试或在寝室考试，为提升考试安全性，需要进行以下操作  &nbsp;&nbsp; [ <strong><a href="/home/support#key">查看图示</a></strong> ]</p>
          <ul>
            <li>1. 请主办方在本网站 <strong><a href="/user/signup">注册</a></strong> 一个企业帐号</li>
            <li>2. 登录后台并新建考试，系统会生成一个考试口令（6位数字），请通知给考生</li>
            <li>3. 考生下载本软件，解压后双击运行Exam.exe，输入口令即可进入考试登录界面</li>
          </ul>                   

