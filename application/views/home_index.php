          <h2 class="page-header">产品简介</h2>
          <p>安全考试客户端（Safe Exam Client）是一款运行于电脑桌面端的可执行程序，支持Windows7及以上系统，可以让您的在线考试更安全。</p>
          <p>本软件采用Chromium内核，本质上就是一款浏览器，但加了一层安全外壳，以实现全屏锁定、窗体前置等考试防作弊等功能。</p>
          <p>本软件是一款纯绿色软件，无需安装，解压即可使用，一般来说，凡是能用Chrome浏览器进行的在线考试，均可使用本软件来增强考试的安全性。</p>

